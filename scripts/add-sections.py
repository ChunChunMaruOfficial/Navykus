dirs = ['public/locales', 'src/i18n/locales']
locales = ['ru', 'en', 'ar', 'de', 'es', 'kk', 'tr', 'uz']

for lang in locales:
    for base_dir in dirs:
        path = os.path.join(base_dir, lang, 'translation.json')
        data = json.load(open(path, 'r', encoding='utf-8'))
        
        if 'legalpage' not in data.get('ui', {}):
            continue
        
        for k, v in new_legalpage_keys.items():
            if k not in data['ui']['legalpage']:
                data['ui']['legalpage'][k] = v
        
        json.dump(data, open(path, 'w', encoding='utf-8'), ensure_ascii=False, indent=2)

print('Done - added 4 new keys (dbLocationTitle/Text, registryInfoTitle/Text) to all 16 locale files')
